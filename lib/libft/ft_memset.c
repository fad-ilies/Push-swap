#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t		i;
	char		*fad;

	i = 0;
	fad = (char *)s;
	while (i < n)
	{
		fad[i] = c;
		i++;
	}
	return (s = fad);
}
